     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Paciente $paciente)
    {
        $citas= Cita::select('*')->where('id_pacientes',$paciente->id)->get();
        $estudios = Estudios_programado::select('estudios_programados.fecha', 'estudios.nombre','estudios_programados.hora')
                ->join('estudios', 'estudios.id', '=', 'estudios_programados.id_estudio')
                ->where('estudios_programados.id_paciente',$paciente->id)->get();
 
        return view('pacientes.show',compact('paciente','citas','estudios'));
    }

